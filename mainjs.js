/*đăng ký*/
function clickclick() {
    var modal = document.querySelector('.modal');
    if (modal.style.display === "none") {
        modal.style.display = "block"; 
    } else {
        modal.style.display = "none"; 
    }
}
function back() {
    var modal = document.querySelector('.modal');
    if (modal.style.display === "block") {
        modal.style.display = "none"; 
    } else {
        modal.style.display = "block"; 
    }
}
